package main

import "fmt"

func main() {
	var i int = 4
	var j int = 2
	var k int = 4

	fmt.Println("i < j = ", i < j)
	fmt.Println("i > j = ", i > j)
	fmt.Println("i >= j = ", i >= j)
	fmt.Println("i <= j = ", i <= j)
	fmt.Println("i == j = ", i == j)
	fmt.Println("i != j = ", i != j)
	fmt.Println("i < k = ", i < k)
	fmt.Println("i > k = ", i > k)
	fmt.Println("i >= k = ", i >= k)
	fmt.Println("i <= k = ", i <= k)
	fmt.Println("i == k = ", i == k)
	fmt.Println("i != k = ", i != k)
}
